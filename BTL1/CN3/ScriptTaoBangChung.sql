/* ====================================================================
   2. TẠO BẢNG CỤC BỘ (LOCAL TABLES)
==================================================================== */
CREATE TABLE NHAN_VIEN (
    MaNV VARCHAR2(20) PRIMARY KEY,
    TenNV NVARCHAR2(100),
    GioiTinh NVARCHAR2(10),
    NgaySinh DATE,
    SDT VARCHAR2(20),
    DiaChi NVARCHAR2(255),
    MaCN VARCHAR2(20),
    ChucVu NVARCHAR2(50),
    NgayVaoLam DATE,
    Luong NUMBER
);

CREATE TABLE KHO_HANG (
    MaSP VARCHAR2(20),
    MaCN VARCHAR2(20),
    SoLuongTon NUMBER,
    PRIMARY KEY (MaSP, MaCN)
);

CREATE TABLE HOA_DON (
    MaHD VARCHAR2(30) PRIMARY KEY,
    MaKH VARCHAR2(30),
    MaNV VARCHAR2(20),
    MaCN VARCHAR2(20),
    NgayLap DATE,
    TongTien NUMBER
);

CREATE TABLE CHI_TIET_HOA_DON (
    MaHD VARCHAR2(30),
    MaSP VARCHAR2(20),
    SoLuong NUMBER,
    DonGia NUMBER,
    ThanhTien NUMBER,
    PRIMARY KEY (MaHD, MaSP)
);


/* ====================================================================
   3. RÀNG BUỘC KHÓA NGOẠI (FOREIGN KEYS)
==================================================================== */
-- Ràng buộc với các Materialized Views
ALTER TABLE SACH_VPP ADD CONSTRAINT FK_SACH_DM FOREIGN KEY (MaDM) REFERENCES DANH_MUC(MaDM);
ALTER TABLE SACH_VPP ADD CONSTRAINT FK_SACH_NXB FOREIGN KEY (MaNXB) REFERENCES NHA_XUAT_BAN(MaNXB);
