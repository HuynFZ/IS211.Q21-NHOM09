-- Ràng buộc với các bảng và MV
ALTER TABLE NHAN_VIEN ADD CONSTRAINT FK_NV_CN FOREIGN KEY (MaCN) REFERENCES CHI_NHANH(MaCN);
ALTER TABLE KHO_HANG ADD CONSTRAINT FK_KHO_SP FOREIGN KEY (MaSP) REFERENCES SACH_VPP(MaSP);
ALTER TABLE KHO_HANG ADD CONSTRAINT FK_KHO_CN FOREIGN KEY (MaCN) REFERENCES CHI_NHANH(MaCN);
ALTER TABLE HOA_DON ADD CONSTRAINT FK_HD_KH FOREIGN KEY (MaKH) REFERENCES KHACH_HANG(MaKH);
ALTER TABLE HOA_DON ADD CONSTRAINT FK_HD_NV FOREIGN KEY (MaNV) REFERENCES NHAN_VIEN(MaNV);
ALTER TABLE HOA_DON ADD CONSTRAINT FK_HD_CN FOREIGN KEY (MaCN) REFERENCES CHI_NHANH(MaCN);
ALTER TABLE CHI_TIET_HOA_DON ADD CONSTRAINT FK_CTHD_HD FOREIGN KEY (MaHD) REFERENCES HOA_DON(MaHD);
